export const MAX_LEVEL = 11
export const GRID_SIZE = 4

/** 觉醒等级：Lv.11+1 = 觉醒 1 阶，罗马数字显示 */
export const ROMAN = ['', 'Ⅰ', 'Ⅱ', 'Ⅲ', 'Ⅳ', 'Ⅴ', 'Ⅵ', 'Ⅶ', 'Ⅷ', 'Ⅸ', 'Ⅹ', 'Ⅺ', 'Ⅻ']

/** 计算"觉醒"显示文本（Lv12+） */
export const getLevelLabel = (level: number): string => {
  if (level <= MAX_LEVEL) return `Lv.${level}`
  const awake = level - MAX_LEVEL
  return `醒·${ROMAN[awake] ?? awake}阶`
}
