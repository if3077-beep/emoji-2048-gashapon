const buildTree = (
  level: Array<[string, string, string]>,
): EmojiNode[] =>
  level.map(([emoji, name, lore], i) => ({
    level: i + 1,
    emoji,
    name,
    lore,
    weight: Math.max(1, 32 - i * 2),
  }))

