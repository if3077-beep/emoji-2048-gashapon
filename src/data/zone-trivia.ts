/**
 * 按 (zone, level) 稳定 hash 选 1 段冷知识
 * - level 1-11 → 用 level 选 (1..3)
 * - 同一 zone+level 每次都返回同一段（稳定）
 */
export function getTriviaFor(zone: ZoneId, level: number): string {
  const pool = ZONE_TRIVIA[zone]
  if (!pool || pool.length === 0) return ''
  const idx = (Math.max(1, level) - 1) % pool.length
  return pool[idx]
}

